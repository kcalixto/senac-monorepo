package com.primeiro_semestre.lista02;

public class ex05 {
    public static void main(String[] args) {
        System.out.println("5. Faça um programa para imprimir os números pares entre 100 e 1");
        
        for (int i = 100; i >= 1; i-=2) {
            System.out.print(i + " ,");
        }

    }
}
