package com.primeiro_semestre.lista02;

public class ex01 {
    public static void main(String[] args) {
        System.out.println("1. Faça um programa que mostre todos os números inteiros de 1 a 50. Use o laço WHILE.");
        int i, maxNum;
        i = 0;
        maxNum = 50;
        System.out.println("Digite um número: ");
        while (i <= maxNum) {
            System.out.println(i);
            i++;
        }

    }
}
